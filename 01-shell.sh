She-Bang
#! is called She-Bang
It denotes the path of interpretor for the remaining lines of the script
In general, it is always best to put SHe-Bang at the start of the script.
Only one She-Bang in a single file

In Redhat family, both shell and bash shell are same
But, it is different for Ubuntu and other families

Comments:
To comment a line, use # at the start of the line.